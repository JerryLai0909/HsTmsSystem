package com.infinity.jerry.yyd_tms.adapter;

import android.support.v4.view.PagerAdapter;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ImageView;

import java.util.List;

/**
 * Created by root on 3/8/17.
 */

public class MainPagerAdapter extends PagerAdapter {

    private List<ImageView> imLists;


    public MainPagerAdapter(List<ImageView> imLists) {
        this.imLists = imLists;
    }

    @Override
    public int getCount() {
        return imLists.size();
    }

    @Override
    public boolean isViewFromObject(View view, Object object) {
        return view == object;
    }

    @Override
    public void destroyItem(ViewGroup container, int position, Object object) {
        container.removeView((View) object);
    }

    @Override
    public Object instantiateItem(ViewGroup container, int position) {
        container.addView(imLists.get(position));
        return imLists.get(position);
    }
}
